package com.example.nhom_8.Object;

public class Top {
    private int idDoAn;
    private String tenDoAn;
    private String image;
    private int soLanBanDuoc;
    private int soLuongDaBan;

    public Top() {
    }

    public int getIdDoAn() {
        return idDoAn;
    }

    public void setIdDoAn(int idDoAn) {
        this.idDoAn = idDoAn;
    }

    public String getTenDoAn() {
        return tenDoAn;
    }

    public void setTenDoAn(String tenDoAn) {
        this.tenDoAn = tenDoAn;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getSoLanBanDuoc() {
        return soLanBanDuoc;
    }

    public void setSoLanBanDuoc(int soLanBanDuoc) {
        this.soLanBanDuoc = soLanBanDuoc;
    }

    public int getSoLuongDaBan() {
        return soLuongDaBan;
    }

    public void setSoLuongDaBan(int soLuongDaBan) {
        this.soLuongDaBan = soLuongDaBan;
    }
}
